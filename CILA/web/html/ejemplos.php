

<center><h1>Ejemplos de código</h1></center>

<p align="justify">
Los apuntes del CILA están escritos en SGML siguiendo el DTD LinuxDoc.
Aunque este formato de documentación (LinuxDoc) está obsoleto, resultó
sumamente  útil  para estos  apuntes  pues  nos permitió  obtener  una
versión  en formato  HTML navegable  y  otra en  format PostScript  de
calidad para  imprimir. Los asistentes al  <i>CILA 2001</i> recibieron
una copia de este último.

<p align="justify">
Tras  terminar el  <i>CILA 2001</i>  congelamos el  desarrollo de  los
apuntes, y  más adelante nos  dimos cuenta de  que el formato  SGML no
sería útil  cuando el  documento creciera. La  versión final  de estos
apuntes está disponible aquí para bajarla en varios formatos:

<ul type="disc">

<li><b>SGML</b>: 
<a href="apuntes/Apuntes_CILA.sgml.tar.gz">empaquetados con tar un comprimido con gzip</a>
o bien 
<a href="apuntes/Apuntes_CILA.sgml.zip">comprimidos con zip</a>

<li><b>HTML</b>:
<a href="apuntes/">navegable</a>,
<a href="apuntes/Apuntes_CILA.html.tar.gz">empaquetados con tar un comprimido con gzip</a>
o bien 
<a href="apuntes/Apuntes_CILA.html.zip">comprimidos con zip</a>

<li><b>PostScript</b>:
<a href="apuntes/Apuntes_CILA.ps.gz">comprimidos con gzip</a>
o bien 
<a href="apuntes/Apuntes_CILA.ps.zip">comprimidos con zip</a>

<li><b>PDF</b>: 
<a href="apuntes/Apuntes_CILA.pdf">sin comprimir</a>

</ul>

